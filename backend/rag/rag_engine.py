_knowledge_base: list[dict] = []


def chunk_text(text: str, chunk_size: int = 800, overlap: int = 100) -> list[str]:
    if len(text) <= chunk_size:
        return [text]
    chunks = []
    start = 0
    while start < len(text):
        chunks.append(text[start:start + chunk_size])
        start += chunk_size - overlap
    return chunks


def doc_id_hash(doc_id: int, chunk_idx: int) -> str:
    return hashlib.md5(f"{doc_id}_{chunk_idx}".encode()).hexdigest()


def search_kb(query: str, sector: Optional[str] = None, top_k: int = 6) -> list[dict]:
    query_words = set(query.lower().split())
    scored = []
    for chunk in _knowledge_base:
        if sector and chunk["sector"] != sector and chunk["sector"] != "general":
            continue
        text = (chunk["title"] + " " + chunk["chunk"]).lower()
        score = sum(1 for w in query_words if w in text)
        if any(w in chunk["title"].lower() for w in query_words):
            score += 3
        if score > 0:
            scored.append((score, chunk))
    scored.sort(key=lambda x: x[0], reverse=True)
    return [c for _, c in scored[:top_k]]


def build_context(chunks: list[dict]) -> str:
    parts = []
    seen = set()
    for chunk in chunks:
        header = f"📋 {chunk['title']}"
        if chunk["doc_number"]:
            header += f" ({chunk['doc_number']})"
        if chunk["url"] and chunk["doc_id"] not in seen:
            header += f"\n🔗 {chunk['url']}"
            seen.add(chunk["doc_id"])
        parts.append(f"{header}\n\n{chunk['chunk']}")
    return ("\n\n" + "─" * 40 + "\n\n").join(parts)


